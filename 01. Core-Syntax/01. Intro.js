/*

INTRODUCTION TO ES6 (ECMAScript 6)

ES6 stands for ECMAScript 6.
ECMAScript is the RULE BOOK for JavaScript.
JavaScript follows these rules.

Think like this:
- ECMAScript = rules
- JavaScript = language that follows those rules
*/

// WHY ECMAScript WAS CREATED?

/*
Before ECMAScript:
- Different browsers behaved differently
- JavaScript code worked in one browser but broke in another

So:
ECMAScript was created to STANDARDIZE JavaScript.
That means:
- Same rules
- Same behavior
- Same output everywhere
*/

/*
ES6 is the 6th version of ECMAScript.

- Released in 2015
- Also called ECMAScript 2015
- It was a BIG update to JavaScript
*/

// WHY ES6 IS IMPORTANT?

/*
Before ES6:
- JavaScript code was long
- Hard to read
- Easy to make mistakes

After ES6:
- Cleaner code
- Shorter syntax
- Easier to understand
*/

// SIMPLE EXAMPLE (OLD vs ES6)

// OLD JavaScript (before ES6)
var name = "Yash";
console.log("Hello " + name);

// Output: Hello Yash


// ES6 JavaScript
const userName = "Yash";
console.log(`Hello ${userName}`);

// Output: Hello Yash

/*
Same result,
but ES6 code is:
- Cleaner
- Easier to read
*/

// WHY ES6 IS IMPORTANT FOR REACT?

/*
React is written using ES6 features.

React heavily uses:
- let / const
- Arrow functions
- Template literals
- Destructuring
- Modules (import / export)

*/

/*
Think of ES6 like:
- A new version of a mobile app

Old version:
- Works
- But slow and messy

New version (ES6):
- Faster
- Cleaner
- Better features
*/

