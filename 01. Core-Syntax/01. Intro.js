/*

INTRODUCTION TO ES6

ES6 stands for ECMAScript 6 (also called ES2015)
ECMAScript is the standard/specification (RULE BOOK for JavaScript)

Think like this:
- ECMAScript = rules
- JavaScript = language that follows those rules
*/

// WHY ECMAScript WAS CREATED?

/*
Before ECMAScript:
- Different browsers behaved differently
- JavaScript code worked in one browser but broke in another

So ECMAScript was created to STANDARDIZE JavaScript.
That means:
- Same rules
- Same behavior
- Same output everywhere
*/

/*
ES6 is the 6th version of ECMAScript.

- Released in 2015
- Also called ECMAScript 2015
- It was a BIG update to JavaScript
*/

// WHY ES6 IS IMPORTANT?

/*
Before ES6:
- JavaScript code was longer
- Harder to read
- Easier to make mistakes

After ES6:
- Cleaner code
- Shorter syntax
- Easier to maintain
*/

// Old style
var nameOld = "Yash";
console.log("Hello " + nameOld);

// ES6 style
const userName = "Rohan";
console.log(`Hello ${userName}`);

/*
Same result,
but ES6 code is:
- Cleaner
- Easier to read
*/

// WHY ES6 IS IMPORTANT FOR REACT?

/*
React is written using ES6 features.

React heavily uses:
- let / const
- Arrow functions
- Template literals
- Destructuring
- Modules (import / export)

*/

/*
Think of ES6 like:
- A new version of a mobile app

Old version:
- Works
- But slow and messy

New version (ES6):
- Faster
- Cleaner
- Better features
*/

